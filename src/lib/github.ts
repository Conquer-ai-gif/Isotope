import { Octokit } from '@octokit/rest';

export function getOctokit(accessToken: string) {
  return new Octokit({ auth: accessToken });
}

export async function pushFragmentToGitHub({
  accessToken, owner, repo, files, commitMessage = 'Generated by Isotope',
}: {
  accessToken: string;
  owner: string;
  repo: string;
  files: { [path: string]: string };
  commitMessage?: string;
}) {
  const octokit = getOctokit(accessToken);

  const { data: repoData } = await octokit.repos.get({ owner, repo });
  const branch = repoData.default_branch;

  const { data: refData } = await octokit.git.getRef({ owner, repo, ref: `heads/${branch}` });
  const latestCommitSha = refData.object.sha;

  const { data: commitData } = await octokit.git.getCommit({ owner, repo, commit_sha: latestCommitSha });
  const baseTreeSha = commitData.tree.sha;

  const blobs = await Promise.all(
    Object.entries(files).map(async ([path, content]) => {
      const { data } = await octokit.git.createBlob({
        owner, repo,
        content: Buffer.from(content).toString('base64'),
        encoding: 'base64',
      });
      return { path, sha: data.sha, mode: '100644' as const, type: 'blob' as const };
    }),
  );

  const { data: newTree } = await octokit.git.createTree({ owner, repo, base_tree: baseTreeSha, tree: blobs });
  const { data: newCommit } = await octokit.git.createCommit({
    owner, repo, message: commitMessage, tree: newTree.sha, parents: [latestCommitSha],
  });
  await octokit.git.updateRef({ owner, repo, ref: `heads/${branch}`, sha: newCommit.sha });

  return { commitSha: newCommit.sha, branch };
}

export async function registerGitHubWebhook({
  accessToken, owner, repo, webhookUrl, secret,
}: {
  accessToken: string; owner: string; repo: string; webhookUrl: string; secret: string;
}) {
  const octokit = getOctokit(accessToken);
  const { data } = await octokit.repos.createWebhook({
    owner, repo,
    config: { url: webhookUrl, content_type: 'json', secret },
    events: ['push'],
    active: true,
  });
  return data.id;
}

export async function deleteGitHubWebhook({
  accessToken, owner, repo, hookId,
}: {
  accessToken: string; owner: string; repo: string; hookId: number;
}) {
  const octokit = getOctokit(accessToken);
  await octokit.repos.deleteWebhook({ owner, repo, hook_id: hookId });
}

export async function getRepoFiles({
  accessToken, owner, repo, branch = 'main',
}: {
  accessToken: string; owner: string; repo: string; branch?: string;
}): Promise<{ [path: string]: string }> {
  const octokit = getOctokit(accessToken);

  const { data: refData } = await octokit.git.getRef({ owner, repo, ref: `heads/${branch}` });
  const { data: commitData } = await octokit.git.getCommit({ owner, repo, commit_sha: refData.object.sha });
  const { data: treeData } = await octokit.git.getTree({ owner, repo, tree_sha: commitData.tree.sha, recursive: '1' });

  const files: { [path: string]: string } = {};
  await Promise.all(
    treeData.tree
      .filter((item) => item.type === 'blob' && item.path && item.sha)
      .map(async (item) => {
        const { data: blob } = await octokit.git.getBlob({ owner, repo, file_sha: item.sha! });
        files[item.path!] = Buffer.from(blob.content, 'base64').toString('utf-8');
      }),
  );
  return files;
}

