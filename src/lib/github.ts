// Generate a clean branch name from a prompt
export function generateBranchName(prompt: string): string {
  const slug = prompt
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '')
    .trim()
    .split(/\s+/)
    .slice(0, 5)
    .join('-')
  const suffix = Math.random().toString(36).slice(2, 7)
  return `isotope/${slug}-${suffix}`
}

// Create a new branch and push files to it
export async function createBranchAndPush({
  accessToken, owner, repo, branchName, files, commitMessage = 'Generated by Isotope',
}: {
  accessToken: string
  owner: string
  repo: string
  branchName: string
  files: { [path: string]: string }
  commitMessage?: string
}) {
  const octokit = getOctokit(accessToken)

  // Get default branch SHA to branch from
  const { data: repoData } = await octokit.repos.get({ owner, repo })
  const defaultBranch = repoData.default_branch

  const { data: refData } = await octokit.git.getRef({ owner, repo, ref: `heads/${defaultBranch}` })
  const latestCommitSha = refData.object.sha

  // Create the new branch
  await octokit.git.createRef({
    owner, repo,
    ref: `refs/heads/${branchName}`,
    sha: latestCommitSha,
  })

  // Push files to the new branch
  const { data: commitData } = await octokit.git.getCommit({ owner, repo, commit_sha: latestCommitSha })
  const baseTreeSha = commitData.tree.sha

  const blobs = await Promise.all(
    Object.entries(files).map(async ([path, content]) => {
      const { data } = await octokit.git.createBlob({
        owner, repo,
        content: Buffer.from(content).toString('base64'),
        encoding: 'base64',
      })
      return { path, sha: data.sha, mode: '100644' as const, type: 'blob' as const }
    }),
  )

  const { data: newTree } = await octokit.git.createTree({ owner, repo, base_tree: baseTreeSha, tree: blobs })
  const { data: newCommit } = await octokit.git.createCommit({
    owner, repo, message: commitMessage, tree: newTree.sha, parents: [latestCommitSha],
  })
  await octokit.git.updateRef({ owner, repo, ref: `heads/${branchName}`, sha: newCommit.sha })

  return { commitSha: newCommit.sha, branchName }
}

// Merge a branch into default branch
export async function mergeBranchToMain({
  accessToken, owner, repo, branchName,
}: {
  accessToken: string
  owner: string
  repo: string
  branchName: string
}) {
  const octokit = getOctokit(accessToken)
  const { data: repoData } = await octokit.repos.get({ owner, repo })

  await octokit.repos.merge({
    owner, repo,
    base: repoData.default_branch,
    head: branchName,
    commit_message: `Merge ${branchName} — via Isotope`,
  })
}

